
### Write a python script that reads the first and last name of a person and returns the full name.###

### requesting for the input of first name ###
First_Name = input("Enter your first name: ")

### requesting for the input of second name ###
Second_Name = input("Enter your second name: ")

### summing up first name and second name as full name ###
Full_Name = First_Name +" "+ Second_Name


### printing the full name ###
print(Full_Name)
